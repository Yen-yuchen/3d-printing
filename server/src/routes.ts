import path from "path";
import fs from "fs";
import { Router } from "express";
import multer from "multer";
import { pool } from "../db";
import jwt from "jsonwebtoken";

/**
 * Main API router for the backend.
 *
 * This router handles:
 * - health checks
 * - user creation and lookup
 * - login and JWT-based authentication
 * - model upload, listing, download, deletion
 * - file-based storage for saved models
 *
 * Note:
 * This file currently uses `any` in many places to avoid TypeScript
 * middleware signature issues. A future improvement would be to replace
 * those usages with proper Express Request/Response/NextFunction types.
 */
export const api: any = Router();

/**
 * Secret used to sign and verify JWT tokens.
 *
 * In production this should always come from environment variables.
 */
const JWT_SECRET = process.env.JWT_SECRET ?? "svp3rs3(r3t";

// ---------- File storage ----------

/**
 * Root directory where uploaded files are stored on disk.
 */
const STORAGE_DIR = path.resolve(process.cwd(), "storage");

/**
 * Ensure the storage directory exists before handling uploads.
 */
fs.mkdirSync(STORAGE_DIR, { recursive: true });

/**
 * Multer instance that saves uploaded files directly to disk
 * using random filenames generated by multer.
 */
const upload: any = multer({ dest: STORAGE_DIR });

/**
 * Multer instance that keeps uploaded files in memory as Buffers.
 *
 * This is used when the application wants to manually control
 * the final output path and filename.
 */
const memoryUpload: any = multer({ storage: multer.memoryStorage() });

// ---------- Auth middleware ----------

/**
 * Middleware that verifies a Bearer token and attaches the authenticated
 * user's ID to `req.user`.
 *
 * Expected header format:
 * Authorization: Bearer <token>
 *
 * On success:
 * - req.user = { user_id: number }
 *
 * On failure:
 * - returns 401 with an error message
 */
const requireAuth: any = (req: any, res: any, next: any) => {
  const authHeader: string = req.headers?.authorization ?? "";
  const token = authHeader.startsWith("Bearer ")
    ? authHeader.slice("Bearer ".length)
    : null;

  if (!token) return res.status(401).json({ error: "missing token" });

  try {
    const decoded = jwt.verify(token, JWT_SECRET) as any;
    const user_id = Number(decoded?.user_id);

    if (!Number.isFinite(user_id) || user_id <= 0) {
      return res.status(401).json({ error: "invalid token payload" });
    }

    req.user = { user_id };
    return next();
  } catch {
    return res.status(401).json({ error: "invalid token" });
  }
};

// ---------- Health ----------

/**
 * GET /health
 *
 * Checks whether the server can successfully communicate with the database.
 *
 * Response:
 * - ok: true/false
 * - dbTime: database timestamp if available
 * - error: included if the database is unavailable
 */
api.get("/health", async (_req: any, res: any) => {
  try {
    const result = await pool.query("select now() as now");
    res.json({ ok: true, dbTime: result.rows[0].now });
  } catch (err) {
    console.error("DB health check failed", err);
    res.json({ ok: false, dbTime: null, error: "db unavailable" });
  }
});

// ---------- Users ----------

/**
 * GET /users
 *
 * Returns all users in ascending user_id order.
 */
api.get("/users", async (_req: any, res: any) => {
  try {
    const result = await pool.query(
      "SELECT user_id, email, name, created_at FROM public.users ORDER BY user_id ASC",
    );
    res.json(result.rows);
  } catch (err) {
    console.error("Failed to read users", err);
    res.status(500).json({ error: "db error" });
  }
});

/**
 * POST /users
 *
 * Creates a new user.
 *
 * Expected body:
 * - email: string
 * - name: string
 *
 * Validation:
 * - both email and name are required
 */
api.post("/users", async (req: any, res: any) => {
  const email = String(req.body?.email ?? "")
    .trim()
    .toLowerCase();
  const name = String(req.body?.name ?? "").trim();

  if (!email || !name) {
    return res.status(400).json({ error: "email and name required" });
  }

  try {
    const result = await pool.query(
      `INSERT INTO public.users (email, name)
       VALUES ($1, $2)
       RETURNING user_id, email, name, created_at`,
      [email, name],
    );
    res.status(201).json(result.rows[0]);
  } catch (err) {
    console.error("Insert failed:", err);
    res.status(500).json({ error: "Database error" });
  }
});

/**
 * GET /users/exists
 *
 * Checks whether a user exists by email.
 *
 * Query params:
 * - email: string
 *
 * Response:
 * - { exists: false }
 * - { exists: true, user: ... }
 *
 * This route is intended as a frontend helper and does not authenticate users.
 */
api.get("/users/exists", async (req: any, res: any) => {
  const email = String(req.query?.email ?? "")
    .trim()
    .toLowerCase();
  if (!email) return res.status(400).json({ error: "email required" });

  try {
    const result = await pool.query(
      `SELECT user_id, email, name, created_at
       FROM public.users
       WHERE lower(email) = $1
       LIMIT 1`,
      [email],
    );

    if (result.rowCount === 0) return res.json({ exists: false });
    return res.json({ exists: true, user: result.rows[0] });
  } catch (err) {
    console.error("Exists check failed:", err);
    res.status(500).json({ error: "db error" });
  }
});

// ---------- Login ----------

/**
 * POST /login/email
 *
 * Logs in a user using an email lookup.
 *
 * Expected body:
 * - email: string
 *
 * If a matching user is found:
 * - a JWT token is returned
 * - user information is returned
 *
 * Token payload:
 * - user_id
 *
 * Token lifetime:
 * - 2 hours
 */
api.post("/login/email", async (req: any, res: any) => {
  const email = String(req.body?.email ?? "")
    .trim()
    .toLowerCase();
  if (!email) return res.status(400).json({ error: "email required" });

  try {
    const result = await pool.query(
      `SELECT user_id, email, name, created_at
       FROM public.users
       WHERE lower(email) = $1
       LIMIT 1`,
      [email],
    );

    if (result.rowCount === 0) {
      return res.status(401).json({ error: "no such user" });
    }

    const user = result.rows[0];

    const token = jwt.sign({ user_id: user.user_id }, JWT_SECRET, {
      expiresIn: "2h",
    });

    return res.json({ token, user });
  } catch (err) {
    console.error("Login failed:", err);
    res.status(500).json({ error: "db error" });
  }
});

/**
 * POST /login
 *
 * Development-only admin login.
 *
 * Expected body:
 * - username
 * - password
 *
 * Credentials are checked against environment variables:
 * - LOGIN_USER
 * - LOGIN_PASS
 *
 * This route is separate from email-based user login and returns a simple
 * dev token string.
 */
api.post("/login", async (req: any, res: any) => {
  const { username, password } = req.body ?? {};
  const envUser = process.env.LOGIN_USER ?? "admin";
  const envPass = process.env.LOGIN_PASS ?? "password";

  if (!username || !password)
    return res.status(400).send("username and password required");
  if (username !== envUser || password !== envPass)
    return res.status(401).send("invalid credentials");

  const token = `devtoken-${Date.now()}`;
  res.json({ token });
});

